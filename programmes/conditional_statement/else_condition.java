package programmes.conditional_statement;

public class else_condition {
    public static void main(String[] args){

        /*  the else statement to specify a block of code to be executed when 'if'
         condition is false  */

         int time = 20;
         if (time<18) {
            System.out.println("Good day.");
         }
         else{
            System.out.println("Good evening.");
         }
    }
}
